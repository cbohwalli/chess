export default function convertNumberToLetter(number) {
    if(number == 1) {
        return "A";
    }

    if(number == 2) {
        return "B";
    }

    if(number == 3) {
        return "C";
    }

    if(number == 4) {
        return "D";
    }

    if(number == 5) {
        return "E";
    }

    if(number == 6) {
        return "F";
    }

    if(number == 7) {
        return "G";
    }

    if(number == 8) {
        return "H";
    }

    return null;
}